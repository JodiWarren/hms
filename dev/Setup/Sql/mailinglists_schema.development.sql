CREATE TABLE IF NOT EXISTS `mailinglists` (
  `id` varchar(10) NOT NULL,
  `web_id` varchar(5) NOT NULL,
  `name` text NOT NULL,
  `date_created` text NOT NULL,
  `email_type_option` int(11) NOT NULL,
  `use_awesomebar` int(11) NOT NULL,
  `default_from_name` text NOT NULL,
  `default_from_email` text NOT NULL,
  `default_subject` text NOT NULL,
  `default_language` text NOT NULL,
  `list_rating` float NOT NULL,
  `subscribe_url_short` text NOT NULL,
  `subscribe_url_long` text NOT NULL,
  `beamer_address` text NOT NULL,
  `visibility` text NOT NULL,
  `member_count` int(11) NOT NULL,
  `unsubscribe_count` int(11) NOT NULL,
  `cleaned_count` int(11) NOT NULL,
  `member_count_since_send` int(11) NOT NULL,
  `unsubscribe_count_since_send` int(11) NOT NULL,
  `cleaned_count_since_send` int(11) NOT NULL,
  `campaign_count` int(11) NOT NULL,
  `grouping_count` int(11) NOT NULL,
  `group_count` int(11) NOT NULL,
  `merge_var_count` int(11) NOT NULL,
  `avg_sub_rate` int(11) NOT NULL,
  `avg_unsub_rate` int(11) NOT NULL,
  `target_sub_rate` int(11) NOT NULL,
  `open_rate` double NOT NULL,
  `click_rate` double NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;
